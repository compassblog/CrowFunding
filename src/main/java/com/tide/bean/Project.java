package com.tide.bean;

public class Project {
    private Integer id;

    private String projectno;

    private String title;

    private Integer targetnum;

    private Integer userid;

    private String releasedate;

    private String enddate;

    private Integer status;

    private Integer backernum;

    private Integer fundnum;

    private Integer type;

    private String intro;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getProjectno() {
        return projectno;
    }

    public void setProjectno(String projectno) {
        this.projectno = projectno;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Integer getTargetnum() {
        return targetnum;
    }

    public void setTargetnum(Integer targetnum) {
        this.targetnum = targetnum;
    }

    public Integer getUserid() {
        return userid;
    }

    public void setUserid(Integer userid) {
        this.userid = userid;
    }

    public String getReleasedate() {
        return releasedate;
    }

    public void setReleasedate(String releasedate) {
        this.releasedate = releasedate;
    }

    public String getEnddate() {
        return enddate;
    }

    public void setEnddate(String enddate) {
        this.enddate = enddate;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getBackernum() {
        return backernum;
    }

    public void setBackernum(Integer backernum) {
        this.backernum = backernum;
    }

    public Integer getFundnum() {
        return fundnum;
    }

    public void setFundnum(Integer fundnum) {
        this.fundnum = fundnum;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getIntro() {
        return intro;
    }

    public void setIntro(String intro) {
        this.intro = intro;
    }
}